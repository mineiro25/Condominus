    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ID=Integer.parseInt(jTextField1.getText());
        String data1=funcao.getData(jDateChooser1);
        
        String proprietario=new String(txtProp.getText());
        int Nif= Integer.parseInt(txtNIF.getText());
        String morada=new String(txtMorada.getText());
        int valor=Integer.parseInt(jTextField2.getText());
        
        try {                                             //adiciona novo pagamento
            if (s.getListaPagamentos().existePag(ID) ) {
                Pagamento p = new Pagamento ("Aceite",data1,Nif,morada,proprietario,ID, valor);
                s.getListaPagamentos().addPagamento(p);

            }
        } catch (DadosJaExistentes ex) {
            Logger.getLogger(RegistarPagamentos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

