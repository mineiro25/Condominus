    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jCheckBox1.isSelected()) {
             dataMax=Integer.parseInt(jTextField3.getText());
        }
        if (jCheckBox2.isSelected()) {
             dataAgravamento=Integer.parseInt(jTextField4.getText());
        }
   
        String dia=funcao.getDia(jDateChooser1);
        int diaInt=Integer.parseInt(dia);
        if (diaInt>=dataMax && diaInt<=dataAgravamento) {                           //multa de 50€ se o pagamento se efetuar entre dia 15 e 20
            int valorx=Integer.parseInt(jTextField2.getText());
            valorx=valorx+50;
            String valory=Integer.toString(valorx);
            jTextField2.setText(valory);
            JOptionPane.showMessageDialog(this, "O valor a pagar é de:" +valory);
            
        }
        if (diaInt>dataAgravamento && 31<=diaInt) {  // multa de 100€ se o pagamento se efetuar entre dia 20 e 31
            int valorx=Integer.parseInt(jTextField2.getText());
            valorx=valorx+100;
            String valory=Integer.toString(valorx);
            jTextField2.setText(valory);
            JOptionPane.showMessageDialog(this, "O valor a pagar é de:" +valory);
            
        }
    }//GEN-LAST:event_jButton5ActionPerformed

