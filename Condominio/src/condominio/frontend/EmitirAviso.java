    private void jButton_pdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_pdfActionPerformed
        try {
            // TODO add your handling code here:
            Document doc=new Document() {};
            PdfWriter writer = PdfWriter.getInstance(doc, new FileOutputStream("avisoPagamento.pdf"));
            doc.open();
            PdfContentByte contentByte = writer.getDirectContent();
            PdfTemplate template = contentByte.createTemplate(500, 500);
            Graphics2D g2 = template.createGraphics(500, 500);
            jPanel2.print(g2);
            g2.dispose();
            contentByte.addTemplate(template, 30, 300);
            doc.close();
        } catch (DocumentException ex) {
            Logger.getLogger(EmitirAviso.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(EmitirAviso.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_pdfActionPerformed

